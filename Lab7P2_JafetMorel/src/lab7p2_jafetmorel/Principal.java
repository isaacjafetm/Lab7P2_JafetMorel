    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        boolean fis = false, astr = false, ing = false;
        for (int i = 0; i < a.size(); i++) {
            if (a.get(i).getTitun().equalsIgnoreCase("Ingeniero")) {
                ing = true;
            } else if (a.get(i).getTitun().equalsIgnoreCase("Fisico")) {
                fis = true;
            } else if (a.get(i).getTitun().equalsIgnoreCase("Astrofisico")) {
                astr = true;
            }
        }
        Naves n = (Naves) cb_nave.getSelectedItem();
        Destino d = (Destino) cb_dest.getSelectedItem();
        boolean max = false;
        if (n.getMaxcant() <= a.size()) {
            max = true;
        }
        if (fis & astr & ing & max) {
            jProgressBar1.setValue(n.getCombprin());
            jProgressBar2.setValue(n.getCombres());
            int dis = d.getDistancia();
            while (jProgressBar1.getValue() >= 0 || dis > 0) {
                try {
                    Thread.sleep(5000);
                    int x = (int) jProgressBar1.getValue();
                    dis = dis - (n.getVeloc() * n.getGasto());
                    int y = x - (dis - n.getVeloc() * n.getGasto());
                    jProgressBar1.setValue(y);
                    jProgressBar1.repaint();
                } catch (InterruptedException ex) {

                }
                while (jProgressBar2.getValue() >= 0 || dis > 0) {
                    try {
                        Thread.sleep(5000);
                        int x = (int) jProgressBar2.getValue();
                        dis = dis - (n.getVeloc() * n.getGasto());
                        int y = x - (dis - n.getVeloc() * n.getGasto());
                        jProgressBar2.setValue(y);
                        jProgressBar2.repaint();
                    } catch (InterruptedException ex) {

                    }

                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "La tripulación debe ser menor o igual a la cantidad máxima de pasajeros de la nave. También se debe llevar un Fisico, Astrofisico e ingeniero. Para mas sobre astronautas y naves vaya a la pestaña de listar.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

