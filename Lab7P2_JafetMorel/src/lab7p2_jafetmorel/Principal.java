    private void jTabbedPane3StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane3StateChanged
        // TODO add your handling code here:
        if (jTabbedPane3.getSelectedIndex() == 0) {
            DefaultTableModel model = (DefaultTableModel) table_ast.getModel();
            model.setRowCount(0);
            adminAst aa = new adminAst("./astronautas.cbm");
            aa.cargarArchivo();
            for (int i = 0; i < aa.getListaAst().size(); i++) {
                Object[] row = {aa.getListaAst().get(i).getId(), aa.getListaAst().get(i).getNombre(), aa.getListaAst().get(i).getApellido(), aa.getListaAst().get(i).getNacionalidad(), aa.getListaAst().get(i).getTitun(), aa.getListaAst().get(i).getContext(), aa.getListaAst().get(i).getPeso(), aa.getListaAst().get(i).getMisiones()};
                model.addRow(row);
            }
            table_ast.setModel(model);
            aa.escribirArchivo();
        } else if (jTabbedPane3.getSelectedIndex() == 1) {
            DefaultTableModel model = (DefaultTableModel) table_nav.getModel();
            model.setRowCount(0);
            adminNav an = new adminNav("./naves.cbm");
            an.cargarArchivo();
            for (int i = 0; i < an.getListaNav().size(); i++) {
                Object[] row = {an.getListaNav().get(i).getNombre(), an.getListaNav().get(i).getIden(), an.getListaNav().get(i).getMaxcant(), an.getListaNav().get(i).getDespegue(), an.getListaNav().get(i).getAterrizaje(), an.getListaNav().get(i).getVeloc(), an.getListaNav().get(i).getMaxdist(), an.getListaNav().get(i).getGasto(), an.getListaNav().get(i).getCombprin(), an.getListaNav().get(i).getCombres()};
                model.addRow(row);
            }
            table_nav.setModel(model);
            an.escribirArchivo();
        } else if (jTabbedPane3.getSelectedIndex() == 2) {
            DefaultTableModel model = (DefaultTableModel) table_des.getModel();
            model.setRowCount(0);
            adminDes ad = new adminDes("./destinos.cbm");
            ad.cargarArchivo();
            for (int i = 0; i < ad.getListaDes().size(); i++) {
                Object[] row = {ad.getListaDes().get(i).getNombre(), ad.getListaDes().get(i).getDistancia(), ad.getListaDes().get(i).getSuperficie(), ad.getListaDes().get(i).getSaturacion()};
                model.addRow(row);
            }
            table_des.setModel(model);
            ad.escribirArchivo();
        }
    }//GEN-LAST:event_jTabbedPane3StateChanged

